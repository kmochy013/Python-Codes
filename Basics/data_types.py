# Text Type:	str
# Numeric Types:	int, float, complex
# Sequence Types:	list, tuple, range
# Mapping Type:	dict
# Set Types:	set, frozenset
# Boolean Type:	bool
# Binary Types:	bytes, bytearray, memoryview
# None Type:	NoneType

# - - - Numeric Types - - - #

#  int type
input1 = 202020
print(type(input1))

# float type
input2 = 2020.52656
print(type(input2))

# complex type (in complex type there we have to input 'j' after the integer)
input3 = 5225563j
print(type(input3))

# - - - Text Type - - - #

# str type
input4 = "Kamrul"
print(type(input4)) 

# Connect 2 str type together
input5 = "Islam"

print(input4 + input5)

print(input4 + " " + input5)

print("My name is " + input4 + " " + input5)

# - - - Boolean Type - - - #
# bool means Boolean Type is actually shown the answer is True or False

x = 8
y = 10

print(x > y)
print(type(x < y))